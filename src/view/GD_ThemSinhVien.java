    public String getEmail(String s, String q)
    {
        String[] arr = s.split("\\s+");
        for(String i : arr) 
        {
            i.toUpperCase();
        }
        String res = String.valueOf(arr[arr.length-1].charAt(0));
        for(int i = 0;i<arr.length-1;i++)
        {
            res += arr[i].charAt(0);
        }
        res += "." + q + "@ptit.edu.vn";
        return res;
    }
